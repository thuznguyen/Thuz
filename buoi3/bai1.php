<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <title>Form Validation</title>
    <script>
        function validateForm() {
            let bookName = document.forms["bookForm"]["bookName"].value;
            let author = document.forms["bookForm"]["author"].value;
            let publisher = document.forms["bookForm"]["publisher"].value;
            let year = document.forms["bookForm"]["year"].value;

            if (bookName == "" || author == "" || publisher == "" || year == "") {
                alert("Tất cả các trường phải được điền!");
                return false;
            }

            if (isNaN(year) || year.length != 4) {
                alert("Năm xuất bản phải là một số có 4 chữ số!");
                return false;
            }

            return true;
        }
    </script>
</head>
<body>
    <h2>Book Form</h2>
    <form name="bookForm" action="process_form.php" method="post" onsubmit="return validateForm()">
        Tên sách: <input type="text" name="bookName"><br><br>
        Tác giả: <input type="text" name="author"><br><br>
        Nhà xuất bản: <input type="text" name="publisher"><br><br>
        Năm xuất bản: <input type="text" name="year"><br><br>
        <input type="submit" value="Submit">
    </form>
